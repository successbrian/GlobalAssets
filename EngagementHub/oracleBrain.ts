export interface GhostThought {
  content: string;
  note: string;
  tropes: string[];
  intensity: 'whisper' | 'murmur' | 'declaration' | 'roar';
  category: 'market' | 'philosophy' | 'meme' | 'lore' | 'meta';
}

export interface GhostPost {
  id: string;
  ghost_content: string;
  ghost_note: string;
  tropes_used: string[];
  source_site: string;
  timestamp: string;
}

export interface OracleConfig {
  siteId: string;
  maxDailyPosts: number;
  cooldownMinutes: number;
  enabledCategories: GhostThought['category'][];
}

